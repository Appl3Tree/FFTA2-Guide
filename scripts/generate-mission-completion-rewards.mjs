import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const sourcePath =
    process.env.FFTA2_GUIDE_SOURCE ??
    path.join(
        root,
        "audit",
        "source-snapshots",
        "gamefaqs-dev-53627-browser-fullbody.txt",
    );
const outputPath = path.join(
    root,
    "src",
    "data",
    "missions",
    "missionCompletionRewards.ts",
);
const checkOnly = process.argv.includes("--check");

const guide = fs.readFileSync(sourcePath, "utf8").replace(/\r\n/g, "\n");
const headings = [...guide.matchAll(/^([A-E][1-5]-\d{2}): (.+)$/gm)];

function multilineField(block, name) {
    const escaped = name.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
    const match = block.match(
        new RegExp(`^${escaped}:\\s*([^\\n]*(?:\\n[ \\t]{2,}[^\\n]+)*)`, "m"),
    );
    return match?.[1]
        .split("\n")
        .map((line) => line.trim())
        .filter(Boolean);
}

function parseAmountLine(line, id, field) {
    const match = line.match(/^(\d+)(?:\s+\(([^)]+)\))?$/);
    if (!match) {
        throw new Error(`${id}: could not parse ${field} row "${line}".`);
    }
    return {
        amount: Number(match[1]),
        label: match[2]
            ? match[2].replace(/\b\w/g, (letter) => letter.toUpperCase())
            : null,
    };
}

const rewards = {};

for (let index = 0; index < headings.length; index += 1) {
    const heading = headings[index];
    const id = heading[1];
    const nextStart = headings[index + 1]?.index ?? guide.length;
    const sectionEnd = guide.indexOf("-=-=-=-=-=-=-=-=-=-=-=", heading.index + 1);
    const end = Math.min(
        nextStart,
        sectionEnd >= 0 ? sectionEnd : guide.length,
    );
    const block = guide.slice(heading.index, end);

    const abilityRows = multilineField(block, "Ability Points");
    if (!abilityRows?.length) {
        throw new Error(`${id}: missing Ability Points.`);
    }
    const abilityPointBreakdown = abilityRows.map((line) =>
        parseAmountLine(line, id, "Ability Points"),
    );

    const clanRows = multilineField(block, "Clan Rewards");
    if (!clanRows?.length) {
        throw new Error(`${id}: missing Clan Rewards.`);
    }

    let clanPoints = 0;
    const talentChanges = {};
    if (clanRows[0] !== "N/A") {
        const clanPointMatch = clanRows[0].match(/^(\d+)\s+Clan Points(?:\s+\([^)]+\))?$/);
        if (!clanPointMatch) {
            throw new Error(`${id}: could not parse Clan Points row "${clanRows[0]}".`);
        }
        clanPoints = Number(clanPointMatch[1]);

        for (const line of clanRows.slice(1)) {
            const talentMatch = line.match(
                /^(Negotiation|Aptitude|Teamwork|Adaptability)\s+\+(\d+)(?:\s+\([^)]+\))?$/,
            );
            if (!talentMatch) {
                throw new Error(`${id}: could not parse clan talent row "${line}".`);
            }
            talentChanges[talentMatch[1].toLowerCase()] = Number(talentMatch[2]);
        }
    } else if (clanRows.length > 1) {
        throw new Error(`${id}: unexpected rows after Clan Rewards N/A.`);
    }

    rewards[id] = {
        abilityPoints: abilityPointBreakdown.reduce(
            (total, entry) => total + entry.amount,
            0,
        ),
        ...(abilityPointBreakdown.length > 1
            ? { abilityPointBreakdown }
            : {}),
        clanPoints,
        ...(Object.keys(talentChanges).length > 0 ? { talentChanges } : {}),
    };
}

if (headings.length !== 300 || Object.keys(rewards).length !== 300) {
    throw new Error(
        `Expected 300 quest reward records, found ${Object.keys(rewards).length}.`,
    );
}

const output = `/**
 * Completion rewards for the 300 quest-report missions.
 *
 * Generated by scripts/generate-mission-completion-rewards.mjs.
 */
import type { ClanTalentChanges, RewardAmountBreakdown } from "../../types/ffta2";

export interface MissionCompletionReward {
    abilityPoints: number;
    abilityPointBreakdown?: readonly RewardAmountBreakdown[];
    clanPoints: number;
    talentChanges?: ClanTalentChanges;
}

export const MISSION_COMPLETION_REWARDS: Readonly<Record<string, MissionCompletionReward>> = ${JSON.stringify(rewards, null, 4)};
`;

if (checkOnly) {
    if (!fs.existsSync(outputPath)) {
        throw new Error(`Missing generated file: ${path.relative(root, outputPath)}`);
    }
    const current = fs.readFileSync(outputPath, "utf8");
    if (current !== output) {
        throw new Error(
            `${path.relative(root, outputPath)} is stale. Run npm run generate:mission-rewards.`,
        );
    }
    console.log(
        `Mission completion rewards are current: ${Object.keys(rewards).length} missions.`,
    );
} else {
    fs.writeFileSync(outputPath, output);
    console.log(
        `Wrote completion rewards for ${Object.keys(rewards).length} missions.`,
    );
}
